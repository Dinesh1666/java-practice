package org.control;

public class Employee {
	private int id;
	private String name;
	private long phno;
	
	
	//setter
	public void setId(int id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPhno(long phno) {
		this.phno = phno;
	}
	
	//getter
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public long getPhno() {
		return phno;
	}

	
	
	
	
	
	
	
	
	
	
	
	

}
